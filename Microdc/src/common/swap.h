#ifndef SWAP_H
#define SWAP_H

#if HAVE_CONFIG_H
#include <config.h>
#endif

#ifndef swap
#define swap(a,b) ({ typeof(a) _a = (a); (a) = (b); (b) = _a; })
#endif

#endif
